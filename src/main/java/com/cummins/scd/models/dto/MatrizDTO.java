package com.cummins.scd.models.dto;

public class MatrizDTO {
	
	private String idMatriz;
	private String noRevision;
	private String nombre;
	public String getIdMatriz() {
		return idMatriz;
	}
	public void setIdMatriz(String idMatriz) {
		this.idMatriz = idMatriz;
	}
	public String getNoRevision() {
		return noRevision;
	}
	public void setNoRevision(String noRevision) {
		this.noRevision = noRevision;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	

}
