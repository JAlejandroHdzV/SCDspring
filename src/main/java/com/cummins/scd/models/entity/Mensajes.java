package com.cummins.scd.models.entity;


public class Mensajes {
	private String line;
	private String message;

	
	
	public String getLine() {
		return line;
	}
	public void setLine(String line) {
		this.line = line;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String mes) {
		this.message = mes;
	}
}

